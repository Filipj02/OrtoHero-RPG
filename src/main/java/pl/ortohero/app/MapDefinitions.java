    private static int[][] parseMapFromCSV(String rawData, int cols, int rows) {
        int[][] map = new int[rows][cols];
        // Dzielimy po nowej linii LUB średniku (jeśli format jest dziwny)
        // Najbezpieczniej dzielić po liniach, a potem po przecinkach
        String[] lines = rawData.trim().split("\n");

        for (int y = 0; y < rows; y++) {
            if (y >= lines.length) break;

            // Obsługa separatorów: przecinek (standard CSV) lub średnik
            String line = lines[y].trim();
            String[] cells;
            if (line.contains(",")) {
                cells = line.split(",");
            } else {
                cells = line.split(";");
            }

            for (int x = 0; x < cols; x++) {
                if (x >= cells.length) break;
                try {
                    int val = Integer.parseInt(cells[x].trim());

                    // --- LEGENDA ---
                    if (val == 0 || val == 1 || val == 2 || val == 3 || val == 8) {
                        map[y][x] = val; // Teren
                    }
                    else if (val == 55) { // BOSS
                        map[y][x] = 8;
                        tempObjects.add(new GameObject("BOSS", "enemy", x*32, y*32));
                    }
                    else if (val == 4) { // SKRZYNIA
                        map[y][x] = 0;
                        tempObjects.add(new GameObject("Skrzynia", "item", x*32, y*32));
                    }
                    else if (val == 6) { // MOST
                        map[y][x] = 2;
                        tempObjects.add(new GameObject("Zawalony Most", "obstacle", x*32, y*32));
                    }
                    else if (val == 7) { // WROTA
                        map[y][x] = 0;
                        tempObjects.add(new GameObject("Zamknięte Wrota", "obstacle", x*32, y*32));
                    }
                    else if (val == 51) { map[y][x]=0; tempObjects.add(new GameObject("Goblin", "enemy", x*32, y*32)); }
                    else if (val == 52) { map[y][x]=0; tempObjects.add(new GameObject("Wilk", "enemy", x*32, y*32)); }
                    else if (val == 53) {
                        map[y][x] = 0;
                        tempObjects.add(new GameObject("Beast", "enemy", x*32, y*32));
                    }

                    // ... wewnątrz metody parseMapFromCSV ...
                    else if (val == 54) {
                        map[y][x] = 0; // Trawa pod spodem
                        tempObjects.add(new GameObject("Troll", "enemy", x*32, y*32));
                    }
                    else if (val == 56) {
                        map[y][x] = 0;
                        tempObjects.add(new GameObject("Slime", "enemy", x*32, y*32));
                    }
                    else if (val == 57) {
                        map[y][x] = 0;
                        tempObjects.add(new GameObject("Nieznany", "enemy", x*32, y*32));
                    }
// ... reszta bez zmian ...

                } catch (Exception e) { map[y][x] = 0; }
            }
        }
        return map;
    }
