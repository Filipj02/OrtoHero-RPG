    private static List<GameObject> tempObjects = new ArrayList<>();

    public static int[][] getMapData(int mapId, int cols, int rows) {
        // 1. Sprawdzamy czy mamy tę mapę w pamięci
        if (cachedTiles.containsKey(mapId)) {
            return cachedTiles.get(mapId);
        }

        // 2. Jeśli nie, wczytujemy z tekstu (tylko raz!)
        String mapData = getRawMapString(mapId);

        if (mapData != null && !mapData.trim().isEmpty()) {
            return parseMapFromCSV(mapData, cols, rows, mapId);
        }

        // Fallback (pusta mapa)
        return new int[rows][cols];
    }

    public static List<GameObject> getObjects(int mapId) {
        // Zwracamy listę z pamięci (jeśli istnieje) lub pustą
        return cachedObjects.getOrDefault(mapId, new ArrayList<>());
    }

    // Metoda czyszcząca pamięć (wywoływana przy Game Over / Restart)
    public static void resetAllMaps() {
        cachedTiles.clear();
        cachedObjects.clear();
    }

    private static String getRawMapString(int newMapId) {
        switch (newMapId) {
            // RZĄD 1 (GÓRA)
            case 1: return MAP_1; // Start
            case 2: return MAP_3;
            case 3: return MAP_4; // Prawa góra

            // RZĄD 2 (ŚRODEK)
            case 4: return MAP_6;
            case 5: return MAP_7; // Środek
            case 6: return MAP_8;

            // RZĄD 3 (DÓŁ)
            case 7: return MAP_10;
            case 8: return MAP_11;
            case 9: return MAP_12; // Koniec (prawa dół)

            default: return "";
        }
    }

    private static int[][] parseMapFromCSV(String rawData, int cols, int rows, int mapId) {
        int[][] map = new int[rows][cols];
        List<GameObject> objectsOnThisMap = new ArrayList<>();

        String[] lines = rawData.trim().split("\n");

        for (int y = 0; y < rows; y++) {
            if (y >= lines.length) break;
            String line = lines[y].trim();
            String[] cells = line.contains(",") ? line.split(",") : line.split(";");

            for (int x = 0; x < cols; x++) {
                if (x >= cells.length) break;
                try {
                    int val = Integer.parseInt(cells[x].trim());

                    // Teren
                    if (val == 0 || val == 1 || val == 2 || val == 3 || val == 8) {
                        map[y][x] = val;
                    }
                    // Obiekty
                    else if (val == 55) { // BOSS
                        map[y][x] = 8;
                        objectsOnThisMap.add(new GameObject("BOSS", "enemy", x*32, y*32));
                    }
                    else if (val == 4) { // SKRZYNIA
                        map[y][x] = 0;
                        objectsOnThisMap.add(new GameObject("Skrzynia", "item", x*32, y*32));
                    }
                    else if (val == 6) { // MOST
                        map[y][x] = 2; // Woda pod spodem
                        objectsOnThisMap.add(new GameObject("Zawalony Most", "obstacle", x*32, y*32));
                    }
                    else if (val == 7) { // WROTA
                        map[y][x] = 0; // Podłoga pod spodem
                        objectsOnThisMap.add(new GameObject("Zamknięte Wrota", "obstacle", x*32, y*32));
                    }
                    // Potwory
                    else if (val == 51) { map[y][x]=0; objectsOnThisMap.add(new GameObject("Goblin", "enemy", x*32, y*32)); }
                    else if (val == 52) { map[y][x]=0; objectsOnThisMap.add(new GameObject("Wilk", "enemy", x*32, y*32)); }
                    else if (val == 53) { map[y][x]=0; objectsOnThisMap.add(new GameObject("Beast", "enemy", x*32, y*32)); }
                    else if (val == 54) { map[y][x]=0; objectsOnThisMap.add(new GameObject("Troll", "enemy", x*32, y*32)); }
                    else if (val == 56) { map[y][x]=0; objectsOnThisMap.add(new GameObject("Slime", "enemy", x*32, y*32)); }
                    else if (val == 57) { map[y][x]=0; objectsOnThisMap.add(new GameObject("Nieznany", "enemy", x*32, y*32)); }

                } catch (Exception e) { map[y][x] = 0; }
            }
        }

        // ZAPISUJEMY WYNIK DO PAMIĘCI PODRĘCZNEJ
        cachedTiles.put(mapId, map);
        cachedObjects.put(mapId, objectsOnThisMap);

        return map;
    }
