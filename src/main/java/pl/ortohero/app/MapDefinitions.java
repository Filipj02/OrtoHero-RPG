    public static int[][] getMapData(int mapId, int cols, int rows) {
        tempObjects.clear();

        // Wybieramy odpowiedni ciąg znaków dla danej mapy
        String mapData = "";

        switch (mapId) {
            case 1: mapData = MAP_1; break;
            case 2: mapData = MAP_2; break;
            case 3: mapData = MAP_3; break;
            case 4: mapData = MAP_4; break;
            case 5: mapData = MAP_5; break;
            case 6: mapData = MAP_6; break;
            case 7: mapData = MAP_7; break;
            case 8: mapData = MAP_8; break;
            case 9: mapData = MAP_9; break;
            case 10: mapData = MAP_10; break;
            case 11: mapData = MAP_11; break;
            case 12: mapData = MAP_12; break;
            case 13: mapData = MAP_13; break;
            case 14: mapData = MAP_14; break;
            case 15: mapData = MAP_15; break;
            case 16: mapData = MAP_16; break;
            default: mapData = ""; break;
        }

        // Jeśli dane zostały wklejone, parsujemy je
        if (mapData != null && !mapData.trim().isEmpty()) {
            return parseMapFromCSV(mapData, cols, rows);
        }

        // --- FALLBACK (Jeśli zmienna jest pusta, generujemy pustą trawę) ---
        int[][] map = new int[rows][cols];
        for(int y=0; y<rows; y++) for(int x=0; x<cols; x++) map[y][x] = 0;

        // Granice świata (żeby nie wypaść poza siatkę 4x4)
        boolean isTop = (mapId >= 1 && mapId <= 4);
        boolean isBottom = (mapId >= 13 && mapId <= 16);
        boolean isLeft = (mapId == 1 || mapId == 5 || mapId == 9 || mapId == 13);
        boolean isRight = (mapId == 4 || mapId == 8 || mapId == 12 || mapId == 16);

        if(isTop) for(int x=0; x<cols; x++) map[0][x] = 1;
        if(isBottom) for(int x=0; x<cols; x++) map[rows-1][x] = 1;
        if(isLeft) for(int y=0; y<rows; y++) map[y][0] = 1;
        if(isRight) for(int y=0; y<rows; y++) map[y][cols-1] = 1;

        return map;
    }

